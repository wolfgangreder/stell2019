  public int getValue()
  {
    int value = 0;
    if (ck1.isSelected()) {
      value |= 0x01;
    }
    if (ck2.isSelected()) {
      value |= 0x02;
    }
    if (ck3.isSelected()) {
      value |= 0x04;
    }
    if (ck4.isSelected()) {
      value |= 0x08;
    }
    if (ck5.isSelected()) {
      value |= 0x10;
    }
    if (ck6.isSelected()) {
      value |= 0x20;
    }
    if (ck8.isSelected()) {
      value |= 0x40;
    }
    if (ck9.isSelected()) {
      value |= 0x80;
    }
    return value;
  }

  public void setValue(int v)
  {
    ck1.setSelected((v & 0x01) != 0);
    ck2.setSelected((v & 0x02) != 0);
    ck3.setSelected((v & 0x04) != 0);
    ck4.setSelected((v & 0x08) != 0);
    ck5.setSelected((v & 0x10) != 0);
    ck6.setSelected((v & 0x20) != 0);
    ck8.setSelected((v & 0x40) != 0);
    ck9.setSelected((v & 0x80) != 0);
  }

  public void addChangeListener(ChangeListener l)
  {
    if (l != null) {
      changeListener.add(l);
    }
  }

  public void removeChangeListener(ChangeListener l)
  {
    changeListener.remove(l);
  }

  private void fireChange()
  {
    ChangeEvent evt = new ChangeEvent(this);
    for (ChangeListener l : changeListener) {
      l.stateChanged(evt);
    }
  }

