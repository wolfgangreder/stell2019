  private final PacketListener packetListener = this::onPacket;
  private boolean listenerConnected;
  private volatile PowerPort port;
  private final Color red = new Color(0xcc0000);
  private final Color blue = new Color(0x0000cc);
  private final Color gray = new Color(0xcccccc);
  private final Color green = new Color(0x00cc00);
  private PowerMode powerMode;

  public PowerControlPanel()
  {
    initComponents();
    assignPowerState(PowerMode.ON);
  }

  @Override
  protected void doLinkStateChanged(LinkState linkState)
  {
    setControlState();
  }

  private void onPacket(ZCAN device,
                        Packet packet)
  {
    PowerStateInfo powerState = packet.getAdapter(PowerStateInfo.class);
    if (powerState != null && powerState.getOutput() == port) {
      SwingUtilities.invokeLater(() -> assignPowerState(powerState.getMode()));
    }
  }

  private void setControlState()
  {
    assignPowerState(powerMode);
  }

  private void enableButton(JButton button,
                            boolean enable,
                            Color colorEn,
                            Color colorDis)
  {
    boolean e = enable && device != null && device.getLinkState() == LinkState.OPEN;
    button.setEnabled(e);
    if (e) {
      button.setBackground(colorEn);
    } else {
      button.setBackground(colorDis);
    }
  }

  private void assignPowerState(PowerMode mode)
  {
    this.powerMode = mode;
    enableButton(btOff,
                 mode != PowerMode.OFF,
                 red,
                 gray);
    enableButton(btSSPEm,
                 mode != PowerMode.SSPE && mode != PowerMode.SSP0,
                 blue,
                 gray);
    enableButton(btSSPF0,
                 mode != PowerMode.SSPE && mode != PowerMode.SSP0,
                 blue,
                 gray);
    enableButton(btOn,
                 mode != PowerMode.ON,
                 green,
                 gray);
  }

  private void setPowerMode(PowerMode mode)
  {
    if (device != null) {
      try {
        device.setPowerModeInfo(port,
                                mode);
      } catch (IOException ex) {
        Exceptions.printStackTrace(ex);
      }
    }
  }

  public PowerPort getPort()
  {
    return port;
  }

  public void setPort(PowerPort port)
  {
    this.port = port;
  }

  @Override
  protected void connectListener()
  {
    if (device != null && !listenerConnected) {
      device.addPacketListener(CommandGroup.SYSTEM,
                               packetListener);
      listenerConnected = true;
    }
    setControlState();
  }

  @Override
  protected void disconnectListener()
  {
    if (listenerConnected) {
      if (device != null) {
        device.removePacketListener(CommandGroup.SYSTEM,
                                    packetListener);
      }
      listenerConnected = false;
    }
    setControlState();
  }

